"""  Write a Python program to read a string, an integer, and a float from
the keyboard and display them."""




text = input("Enter a string: ")

integer_value = int(input("Enter an integer: "))

float_value = float(input("Enter a float: "))

# Display the inputs
print(f"You entered the string: {text}")
print(f"You entered the integer: {integer_value}")
print(f"You entered the float: {float_value}")